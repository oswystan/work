function boost_dl() {
    echo "##############################################"
    echo "###########  DOWNLOADING  ####################"
    echo "##############################################"
    wget https://dl.bintray.com/boostorg/release/1.65.1/source/boost_1_65_1.tar.bz2
    tar jxvf boost_1_65_1.tar.bz2
}
function boost_mk() {
    echo "##############################################"
    echo "################ BUILDING ####################"
    echo "##############################################"
    cur_dir=`pwd`
    cd boost_1_65_1
    ./bootstrap.sh --prefix=$cur_dir
    ./b2 -j4
    ./b2 install
    cd $cur_dir
    echo "##############################################"
    echo "#################### DONE ####################"
    echo "##############################################"
}



###########################################################################
boost_dl
boost_mk
